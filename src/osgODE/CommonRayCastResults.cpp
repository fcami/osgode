/* ======================================================================= */
/* ....................................................................... */
AllRayCastResult::AllRayCastResult(const AllRayCastResult& other):
    Space::RayCastResult(other),
    m_collision_list( other.m_collision_list )
{
}
/* ....................................................................... */
/* ======================================================================= */




/* ======================================================================= */
/* ....................................................................... */
